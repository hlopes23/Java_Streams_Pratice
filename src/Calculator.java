public class Calculator {
    //definir caracteristicas do objeto Calculator

    int firstnumber;
    int secondnumber;

    public int sum () {
      return firstnumber + secondnumber;

    }

    public int subtract () {
        return firstnumber - secondnumber;

    }

    public int multiply () {
        return firstnumber * secondnumber;

    }

    public int division () {
        return firstnumber/secondnumber;
    }
}


